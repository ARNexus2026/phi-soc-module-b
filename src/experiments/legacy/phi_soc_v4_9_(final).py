# OTIMIZAÇÃO CEGA (V4.9 — ANTI-COLAPSO CORRIGIDO)
# =========================================
def optimize_blind(F_raw, g, F_ext, epochs=2000):
    K = F_raw.shape[0]
    F_lie = torch.einsum('jm, imk -> ijk', g, F_raw) - torch.einsum('im, jmk -> ijk', g, F_raw)

    alpha = torch.nn.Parameter(0.01 * torch.randn((8, K), device=device))
    optimizer = optim.Adam([alpha], lr=1e-3)

    for epoch in range(epochs):
        optimizer.zero_grad()
        norms = alpha.norm(dim=1, keepdim=True) + 1e-8
        alpha_norm = alpha / norms

        Ftilde_raw = torch.einsum('ai,bj,ck,ijk->abc', alpha_norm, alpha_norm, alpha_norm, F_lie)
        Ftilde_raw = torch.clamp(Ftilde_raw, -10, 10)
        norm_F = Ftilde_raw.norm() + 1e-8
        Ftilde = Ftilde_raw / norm_F

        loss_antisym = (Ftilde + Ftilde.transpose(0, 1)).pow(2).mean()
        loss_jac = jacobi_error(Ftilde)

        Gproj = torch.einsum('ai,ij,bj->ab', alpha_norm, g, alpha_norm)
        loss_ort = (Gproj - torch.eye(8, device=device)).pow(2).mean()

        leakage = torch.einsum('ai, bj, ijm -> abm', alpha_norm, alpha_norm, F_ext)
        loss_ext = leakage.pow(2).mean()

        loss_closure = closure_error_diff(F_lie, alpha_norm)

        # Anti-colapso CORRIGIDO: penaliza autovalores pequenos via log
        K_ftilde = torch.einsum('acd,bcd->ab', Ftilde, Ftilde)
        eigvals_K = torch.abs(torch.linalg.eigvalsh(K_ftilde))
        loss_dim = -torch.sum(torch.log(eigvals_K + 1e-8))

        # Penalização adicional: penaliza autovalores próximos de zero
        loss_rank = (eigvals_K < 1e-4).float().mean()

        loss = (
            loss_antisym
            + 0.1 * loss_jac
            + 0.1 * loss_ort
            + 0.01 * loss_ext
            + 0.1 * loss_closure
            + 0.01 * loss_dim      # peso aumentado
            + 0.1 * loss_rank      # NOVO: penaliza rank baixo
        )

        loss.backward()
        torch.nn.utils.clip_grad_norm_([alpha], 1.0)
        optimizer.step()

        if epoch % 200 == 0:
            eig_numpy = eigvals_K.detach().cpu().numpy()
            dim_est = (eig_numpy > 1e-4).sum()
            print(f"Epoch {epoch:4d} | Loss: {loss.item():.6f} | Clos: {loss_closure.item():.6f} | Dim: {loss_dim.item():.2f} | d_est: {dim_est} | Rank: {loss_rank.item():.4f}")

    return Ftilde.detach(), Gproj.detach(), loss.item(), F_lie, alpha_norm.detach()

