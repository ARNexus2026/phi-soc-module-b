import torch
import torch.optim as optim
import networkx as nx
import random
import numpy as np

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
print("Device:", device)

# =========================================
# GRAPH
# =========================================
def generate_graph(N, m=2):
    return nx.barabasi_albert_graph(N, m)

def spanning_tree(G):
    return nx.bfs_tree(G, source=list(G.nodes())[0])

# =========================================
# CYCLES
# =========================================
def path_to_edges(path):
    return [(path[i], path[i+1]) for i in range(len(path)-1)]

def fundamental_cycles(G, T):
    cycles = []
    T = T.to_undirected()
    for (u, v) in G.edges():
        if not T.has_edge(u, v):
            try:
                path = nx.shortest_path(T, u, v)
                edges = path_to_edges(path)
                edges.append((v, u))
                cycles.append(edges)
            except:
                pass
    return cycles

# =========================================
# CLUSTER
# =========================================
def select_cluster(G, cycles, max_size=64):
    hub = max(G.degree, key=lambda x: x[1])[0]
    cluster = [c for c in cycles if any(hub in e for e in c)]
    return cluster[:max_size]

# =========================================
# EDGE MAPS
# =========================================
def canonical_edge(u, v):
    return (min(u, v), max(u, v))

def build_edge_maps(cycles):
    maps = []
    for cycle in cycles:
        m = {}
        for (u, v) in cycle:
            key = canonical_edge(u, v)
            sign = 1 if (u, v) == key else -1
            m[key] = sign
        maps.append(m)
    return maps

# =========================================
# STRUCTURE TENSOR F
# =========================================
def compute_F(edge_maps):
    K = len(edge_maps)
    F = torch.zeros((K, K, K), device=device)

    edge_to_cycles = {}
    for i, m in enumerate(edge_maps):
        for e in m:
            edge_to_cycles.setdefault(e, []).append(i)

    for e, idxs in edge_to_cycles.items():
        for i in idxs:
            for j in idxs:
                for k in idxs:
                    F[i, j, k] += (
                        edge_maps[i][e] *
                        edge_maps[j][e] *
                        edge_maps[k][e]
                    )

    max_val = F.abs().max()
    if max_val > 0:
        F /= max_val
    return F

# =========================================
# METRIC
# =========================================
def compute_metric(edge_maps):
    K = len(edge_maps)
    g = torch.zeros((K, K), device=device)

    for i in range(K):
        for j in range(K):
            g[i, j] = len(set(edge_maps[i]) & set(edge_maps[j]))

    max_val = g.abs().max()
    if max_val > 0:
        g /= max_val
    return g

