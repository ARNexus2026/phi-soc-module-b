# CHEVALLEY (A1, A2)
# =========================================
def chevalley_tensor(algebra='A2'):
    """Retorna o tensor de Chevalley para a álgebra especificada."""
    N = torch.zeros((8, 8, 8), device=device)

    def add(a, b, c, val):
        N[a, b, c] = val
        N[b, a, c] = -val

    if algebra == 'A1':
        add(0, 1, 2, 1)
        add(0, 2, 1, -1)
        add(1, 2, 0, 1)

    elif algebra == 'A2':
        add(0, 2, 2, 2);  add(0, 3, 3, -2)
        add(0, 4, 4, -1); add(0, 5, 5, 1)
        add(0, 6, 6, 1);  add(0, 7, 7, -1)
        add(1, 2, 2, -1); add(1, 3, 3, 1)
        add(1, 4, 4, 2);  add(1, 5, 5, -2)
        add(1, 6, 6, 1);  add(1, 7, 7, -1)
        add(2, 4, 6, 1);  add(2, 7, 5, -1)
        add(4, 7, 3, 1);  add(2, 3, 0, 1)
        add(4, 5, 1, 1);  add(6, 7, 0, 1)
        add(6, 7, 1, 1)

    return N

def all_reference_tensors():
    refs = {}
    for name in ['A1', 'A2']:
        refs[name] = chevalley_tensor(name)
    return refs

# =========================================
# JACOBI
# =========================================
def jacobi_error(F):
    t1 = torch.einsum('abd,dce->abce', F, F)
    t2 = torch.einsum('bcd,dae->abce', F, F)
    t3 = torch.einsum('cad,dbe->abce', F, F)
    return (t1 + t2 + t3).pow(2).mean()

# =========================================
# FECHAMENTO (DIFERENCIÁVEL)
# =========================================
def closure_error_diff(F_lie, alpha):
    brackets = torch.einsum('ai,bj,ijk->abk', alpha, alpha, F_lie)
    coef = torch.einsum('ai,abk->ibk', alpha, brackets)
    proj = torch.einsum('ai,ibk->abk', alpha, coef)
    return (brackets - proj).pow(2).mean()

# =========================================
# EXTERNAL TENSOR
# =========================================
def compute_F_ext(cluster_maps, all_maps, samples=30):
    K = len(cluster_maps)
    external = [m for m in all_maps if m not in cluster_maps]
    if len(external) == 0:
        return torch.zeros((K, K, 1), device=device)
    sampled = random.sample(external, min(samples, len(external)))
    M = len(sampled)
    F_ext = torch.zeros((K, K, M), device=device)
    for m_idx, ext in enumerate(sampled):
        for e, ext_val in ext.items():
            for i in range(K):
                if e in cluster_maps[i]:
                    for j in range(K):
                        if e in cluster_maps[j]:
                            F_ext[i, j, m_idx] += cluster_maps[i][e] * cluster_maps[j][e] * ext_val
    max_val = F_ext.abs().max()
    if max_val > 0:
        F_ext /= max_val
    return F_ext

