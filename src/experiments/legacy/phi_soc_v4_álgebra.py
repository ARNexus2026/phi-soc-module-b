import torch
import torch.optim as optim
import networkx as nx
import random
import numpy as np

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
print("Device:", device)

# =========================================
# GRAPH
# =========================================
def generate_graph(N, m=2):
    return nx.barabasi_albert_graph(N, m)

def spanning_tree(G):
    return nx.bfs_tree(G, source=list(G.nodes())[0])

# =========================================
# CYCLES
# =========================================
def path_to_edges(path):
    return [(path[i], path[i+1]) for i in range(len(path)-1)]

def fundamental_cycles(G, T):
    cycles = []
    T = T.to_undirected()
    for (u, v) in G.edges():
        if not T.has_edge(u, v):
            try:
                path = nx.shortest_path(T, u, v)
                edges = path_to_edges(path)
                edges.append((v, u))
                cycles.append(edges)
            except:
                pass
    return cycles

# =========================================
# CLUSTER
# =========================================
def select_cluster(G, cycles, max_size=64):
    hub = max(G.degree, key=lambda x: x[1])[0]
    cluster = [c for c in cycles if any(hub in e for e in c)]
    return cluster[:max_size]

# =========================================
# EDGE MAPS
# =========================================
def canonical_edge(u, v):
    return (min(u, v), max(u, v))

def build_edge_maps(cycles):
    maps = []
    for cycle in cycles:
        m = {}
        for (u, v) in cycle:
            key = canonical_edge(u, v)
            sign = 1 if (u, v) == key else -1
            m[key] = sign
        maps.append(m)
    return maps

# =========================================
# STRUCTURE TENSOR F
# =========================================
def compute_F(edge_maps):
    K = len(edge_maps)
    F = torch.zeros((K, K, K), device=device)

    edge_to_cycles = {}
    for i, m in enumerate(edge_maps):
        for e in m:
            edge_to_cycles.setdefault(e, []).append(i)

    for e, idxs in edge_to_cycles.items():
        for i in idxs:
            for j in idxs:
                for k in idxs:
                    F[i, j, k] += (
                        edge_maps[i][e] *
                        edge_maps[j][e] *
                        edge_maps[k][e]
                    )

    max_val = F.abs().max()
    if max_val > 0:
        F /= max_val
    return F

# =========================================
# METRIC
# =========================================
def compute_metric(edge_maps):
    K = len(edge_maps)
    g = torch.zeros((K, K), device=device)

    for i in range(K):
        for j in range(K):
            g[i, j] = len(set(edge_maps[i]) & set(edge_maps[j]))

    max_val = g.abs().max()
    if max_val > 0:
        g /= max_val
    return g

# =========================================
# CHEVALLEY (A1, A2)
# =========================================
def chevalley_tensor(algebra='A2'):
    """Retorna o tensor de Chevalley para a álgebra especificada."""
    N = torch.zeros((8, 8, 8), device=device)

    def add(a, b, c, val):
        N[a, b, c] = val
        N[b, a, c] = -val

    if algebra == 'A1':   # su(2) — dim 3
        add(0, 1, 2, 1)
        add(0, 2, 1, -1)
        add(1, 2, 0, 1)

    elif algebra == 'A2': # su(3) — dim 8
        add(0, 2, 2, 2);  add(0, 3, 3, -2)
        add(0, 4, 4, -1); add(0, 5, 5, 1)
        add(0, 6, 6, 1);  add(0, 7, 7, -1)
        add(1, 2, 2, -1); add(1, 3, 3, 1)
        add(1, 4, 4, 2);  add(1, 5, 5, -2)
        add(1, 6, 6, 1);  add(1, 7, 7, -1)
        add(2, 4, 6, 1);  add(2, 7, 5, -1)
        add(4, 7, 3, 1);  add(2, 3, 0, 1)
        add(4, 5, 1, 1);  add(6, 7, 0, 1)
        add(6, 7, 1, 1)

    return N

# =========================================
# TENSORES DE REFERÊNCIA
# =========================================
def all_reference_tensors():
    """Retorna dicionário com todos os tensores de Chevalley disponíveis."""
    refs = {}
    for name in ['A1', 'A2']:
        refs[name] = chevalley_tensor(name)
    return refs

# =========================================
# JACOBI
# =========================================
def jacobi_error(F):
    t1 = torch.einsum('abd,dce->abce', F, F)
    t2 = torch.einsum('bcd,dae->abce', F, F)
    t3 = torch.einsum('cad,dbe->abce', F, F)
    return (t1 + t2 + t3).pow(2).mean()

# =========================================
# FECHAMENTO (CORRIGIDO — NO ESPAÇO ORIGINAL)
# =========================================
def closure_error(F_lie, alpha, V):
    """
    Mede o fechamento do subespaço span(V) sob o colchete induzido por F_lie.
    F_lie: (K, K, K) — tensor de estrutura no espaço original
    alpha: (8, K) — coeficientes dos geradores
    V: (8, d_active) — base do subespaço ativo
    """
    K = F_lie.shape[0]
    d = V.shape[1]
    brackets = torch.einsum('ai,bj,ijk->abk', alpha, alpha, F_lie)
    err = 0.0
    for a in range(d):
        for b in range(d):
            bracket_ab = brackets[a, b, :]
            proj = alpha.T @ (alpha @ bracket_ab)
            err += (bracket_ab - proj).pow(2).sum().item()
    return err / (d * d) if d > 0 else 0.0

# =========================================
# EXTERNAL TENSOR (PRÉ-COMPUTADO)
# =========================================
def compute_F_ext(cluster_maps, all_maps, samples=30):
    K = len(cluster_maps)
    external = [m for m in all_maps if m not in cluster_maps]
    if len(external) == 0:
        return torch.zeros((K, K, 1), device=device)
    sampled = random.sample(external, min(samples, len(external)))
    M = len(sampled)
    F_ext = torch.zeros((K, K, M), device=device)
    for m_idx, ext in enumerate(sampled):
        for e, ext_val in ext.items():
            for i in range(K):
                if e in cluster_maps[i]:
                    for j in range(K):
                        if e in cluster_maps[j]:
                            F_ext[i, j, m_idx] += cluster_maps[i][e] * cluster_maps[j][e] * ext_val
    max_val = F_ext.abs().max()
    if max_val > 0:
        F_ext /= max_val
    return F_ext

# =========================================
# OTIMIZAÇÃO SEM ALVO (IDENTIFICAÇÃO AUTOMÁTICA)
# =========================================
def optimize_blind(F_raw, g, F_ext, epochs=1500):
    """
    Otimiza sem um tensor alvo, procurando QUALQUER álgebra de Lie
    que feche bem e desacople do ambiente.
    Retorna: (Ftilde, Gproj, loss, F_lie, alpha_norm)
    """
    K = F_raw.shape[0]
    F_lie = torch.einsum('jm, imk -> ijk', g, F_raw) - torch.einsum('im, jmk -> ijk', g, F_raw)

    alpha = torch.nn.Parameter(0.01 * torch.randn((8, K), device=device))
    optimizer = optim.Adam([alpha], lr=1e-3)

    for epoch in range(epochs):
        optimizer.zero_grad()

        norms = alpha.norm(dim=1, keepdim=True) + 1e-8
        alpha_norm = alpha / norms

        Ftilde_raw = torch.einsum('ai,bj,ck,ijk->abc', alpha_norm, alpha_norm, alpha_norm, F_lie)
        Ftilde_raw = torch.clamp(Ftilde_raw, -10, 10)

        norm_F = Ftilde_raw.norm() + 1e-8
        Ftilde = Ftilde_raw / norm_F

        loss_antisym = (Ftilde + Ftilde.transpose(0, 1)).pow(2).mean()
        loss_jac = jacobi_error(Ftilde)

        Gproj = torch.einsum('ai,ij,bj->ab', alpha_norm, g, alpha_norm)
        loss_ort = (Gproj - torch.eye(8, device=device)).pow(2).mean()

        leakage = torch.einsum('ai, bj, ijm -> abm', alpha_norm, alpha_norm, F_ext)
        loss_ext = leakage.pow(2).mean()

        loss = loss_antisym + 0.1 * loss_jac + 0.1 * loss_ort + 0.01 * loss_ext

        loss.backward()
        torch.nn.utils.clip_grad_norm_([alpha], 1.0)
        optimizer.step()

        if epoch % 200 == 0:
            print(f"Epoch {epoch:4d} | Loss: {loss.item():.6f} | AntiSym: {loss_antisym.item():.6f} | Jac: {loss_jac.item():.6f}")

    return Ftilde.detach(), Gproj.detach(), loss.item(), F_lie, alpha_norm.detach()

