# PIPELINE DE IDENTIFICAÇÃO
# =========================================
def run_identification(N=1000):
    G = generate_graph(N)
    T = spanning_tree(G)
    cycles = fundamental_cycles(G, T)
    print("Total cycles:", len(cycles))

    cluster = select_cluster(G, cycles)
    if not cluster:
        print("Cluster inválido, pulando.")
        return None
    print("Cluster size:", len(cluster))

    cluster_maps = build_edge_maps(cluster)
    all_maps = build_edge_maps(cycles)

    F_raw = compute_F(cluster_maps)
    g = compute_metric(cluster_maps)
    F_ext = compute_F_ext(cluster_maps, all_maps)

    Ftilde, Gproj, loss, F_lie, alpha = optimize_blind(F_raw, g, F_ext)
    name, score, results, jac_post, clos_post = classify_algebra(Ftilde, Gproj, F_lie, alpha)

    print(f"\nÁlgebra identificada: {name} | Score: {score:.6f} | Loss final: {loss:.6f} | Jacobi: {jac_post:.6f} | Fechamento: {clos_post:.6f}")
    for n, r in results.items():
        if 'note' in r:
            print(f"  {n}: {r['note']}")
        else:
            print(f"  {n}: dist={r['dist']:.6f}, killing_err={r['killing_err']:.6f}, score={r['score']:.6f}")
    return name, score, loss

def run_batch_identification(N=1000, runs=10):
    results = []
    for i in range(runs):
        print(f"\n=== RUN {i} ===")
        res = run_identification(N)
        if res is not None:
            results.append(res)
    return results

