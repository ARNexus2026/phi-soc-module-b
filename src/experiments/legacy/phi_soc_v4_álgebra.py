# CLASSIFICAÇÃO AUTOMÁTICA (V4.7 — PROJEÇÃO CORRIGIDA)
# =========================================
def classify_algebra(Ftilde, Gproj, F_lie, alpha, epochs_align=500, n_starts=5):
    """
    Classifica Ftilde após projetar para o subespaço ativo.
    CORREÇÃO: usa V.T no terceiro operador do einsum da projeção.
    """
    K_ftilde = torch.einsum('acd,bcd->ab', Ftilde, Ftilde)
    eigvals, eigvecs = torch.linalg.eigh(K_ftilde)
    abs_vals = eigvals.abs()

    idx_sort = torch.argsort(abs_vals, descending=True)
    eigvals = eigvals[idx_sort]
    eigvecs = eigvecs[:, idx_sort]
    abs_vals = eigvals.abs()

    sorted_vals, _ = torch.sort(abs_vals, descending=True)
    ratios = sorted_vals[:-1] / (sorted_vals[1:] + 1e-8)
    gaps = (ratios > 5.0).int()
    if gaps.sum() > 0:
        d_active = torch.argmax(gaps).item() + 1
    else:
        d_active = (abs_vals > 1e-3 * abs_vals.max()).sum().item()

    print(f"Dimensão ativa (gap espectral): {d_active}")
    if d_active == 0:
        return 'degenerate', 1e6, {}, 0.0, 0.0

    V = eigvecs[:, :d_active]

    # CORREÇÃO: V.T no terceiro operador
    F_active = torch.einsum('ia,jb,kc,abc->ijk', V.T, V.T, V.T, Ftilde)

    jac_post = jacobi_error(F_active).item()
    clos_post = closure_error(F_lie, alpha, V)
    print(f"Jacobi pós-proj: {jac_post:.6f} | Fechamento (original): {clos_post:.6f}")

    refs = all_reference_tensors()
    best_name = None
    best_score = float('inf')
    results = {}

    for name, N_ref in refs.items():
        K_ref_full = torch.einsum('acd,bcd->ab', N_ref, N_ref)
        eig_ref_full, eigvec_ref_full = torch.linalg.eigh(K_ref_full)
        idx_ref = torch.argsort(eig_ref_full.abs(), descending=True)
        eig_ref_full = eig_ref_full[idx_ref]
        eigvec_ref_full = eigvec_ref_full[:, idx_ref]
        d_ref = (eig_ref_full.abs() > 1e-6).sum().item()

        if d_active < d_ref:
            results[name] = {'dist': float('inf'), 'killing_err': float('inf'), 'note': f'dim mismatch (d_active={d_active} < d_ref={d_ref})'}
            continue

        if d_active == d_ref and d_active == 8:
            F_comp = Ftilde
            N_comp = N_ref
        elif d_active == d_ref:
            V_ref = eigvec_ref_full[:, :d_active]
            # CORREÇÃO: V_ref.T no terceiro operador
            N_comp = torch.einsum('ia,jb,kc,abc->ijk', V_ref.T, V_ref.T, V_ref.T, N_ref)
            F_comp = F_active
        else:
            V_ref = eigvec_ref_full[:, :d_active]
            # CORREÇÃO: V_ref.T no terceiro operador
            N_comp = torch.einsum('ia,jb,kc,abc->ijk', V_ref.T, V_ref.T, V_ref.T, N_ref)
            F_comp = F_active

        dim = F_comp.shape[0]
        best_dist = float('inf')
        best_N_trans = None
        for start in range(n_starts):
            P_raw = torch.nn.Parameter(0.1 * torch.randn((dim, dim), device=device) + torch.eye(dim, device=device))
            opt_P = optim.Adam([P_raw], lr=0.01)
            for _ in range(epochs_align):
                opt_P.zero_grad()
                Q, R = torch.linalg.qr(P_raw + 1e-6 * torch.eye(dim, device=device))
                P_orth = Q
                P_inv = P_orth.T
                N_trans = torch.einsum('ai,bj,kc,ijk->abc', P_orth, P_orth, P_inv, N_comp)
                loss_align = (F_comp - N_trans).pow(2).mean()
                loss_align.backward()
                opt_P.step()
            with torch.no_grad():
                Q, R = torch.linalg.qr(P_raw + 1e-6 * torch.eye(dim, device=device))
                P_orth = Q
                P_inv = P_orth.T
                N_trans = torch.einsum('ai,bj,kc,ijk->abc', P_orth, P_orth, P_inv, N_comp)
                dist = (F_comp - N_trans).pow(2).mean().item()
                if dist < best_dist:
                    best_dist = dist
                    best_N_trans = N_trans

        K_active_comp = torch.einsum('acd,bcd->ab', F_comp, F_comp)
        K_ref_comp = torch.einsum('acd,bcd->ab', best_N_trans, best_N_trans)
        eig_active_comp = torch.linalg.eigvalsh(K_active_comp)
        eig_ref_comp = torch.linalg.eigvalsh(K_ref_comp)
        eig_active_comp = eig_active_comp / (torch.norm(eig_active_comp) + 1e-8)
        eig_ref_comp = eig_ref_comp / (torch.norm(eig_ref_comp) + 1e-8)
        killing_err = (eig_active_comp - eig_ref_comp).pow(2).mean().item()

        dist_norm = best_dist / (F_comp.pow(2).mean().item() + 1e-8)
        killing_norm = killing_err / (eig_active_comp.pow(2).mean().item() + 1e-8)
        jac_norm = jac_post / (1.0 + jac_post)
        clos_norm = clos_post / (1.0 + clos_post)

        score = dist_norm + killing_norm + 0.1 * jac_norm + 0.1 * clos_norm
        results[name] = {'dist': best_dist, 'killing_err': killing_err, 'score': score}
        if score < best_score:
            best_score = score
            best_name = name

    return best_name, best_score, results, jac_post, clos_post

