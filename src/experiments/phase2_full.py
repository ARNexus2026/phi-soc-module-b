# =========================================
# Lambda Phi‑SOC — FASE II
# =========================================
import torch
import torch.optim as optim
import networkx as nx
import random
import numpy as np
from collections import deque

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
print("Device:", device)

# =========================
# PARÂMETROS DA SIMULAÇÃO SOC
# =========================
N = 2000               # Tamanho do grafo (pode aumentar para 5000 depois)
m = 2                  # Arestas por novo nó (Barabási‑Albert)
T_equil = 5000         # Passos de equilibração
T_sample = 20000       # Passos de amostragem
snap_interval = 200    # Intervalo entre snapshots (total ≈ 100 snapshots)

eta_lambda = 0.02      # Taxa de adaptação de λ
alpha_smooth = 0.05    # Suavização do R0
threshold_base = 2.0   # Limiar base de tensão

a_SOC = 0.08           # Parâmetros SOC
b_SOC = 0.04
c_SOC = 0.03

# =========================
# GRAFO
# =========================
G = nx.barabasi_albert_graph(N, m)
deg = dict(G.degree())
print(f"Grafo gerado: N={N}, arestas={G.number_of_edges()}")

# =========================
# ESTADO SOC
# =========================
tension = np.zeros(N)            # tensão nos vértices
lambda_val = 1.0                 # inércia histórica inicial
R0_smooth = 1.0                  # estimativa suave de R0

edge_weight_sum = {e: 0.0 for e in G.edges()}
edge_weight_sum.update({(v, u): 0.0 for (u, v) in G.edges()})
snapshots = []

def run_avalanche(start):
    queue = deque([start])
    visited = set([start])
    total_topplings = 0
    while queue:
        i = queue.popleft()
        total_topplings += 1
        for j in G.neighbors(i):
            transfer = (0.3 + b_SOC * lambda_val) * tension[i] / deg[i]
            tension[j] += transfer
            edge_weight_sum[(i, j)] += transfer
            edge_weight_sum[(j, i)] += transfer
            threshold = threshold_base * deg[j] / (1 + a_SOC * lambda_val)
            if tension[j] > threshold and j not in visited:
                visited.add(j)
                queue.append(j)
        dissipation = 0.1 / (1 + c_SOC * lambda_val)
        tension[i] *= dissipation
    return total_topplings, len(visited)

# =========================
# LOOP PRINCIPAL SOC
# =========================
print("Iniciando simulação SOC...")
for t in range(T_equil + T_sample):
    i = random.randint(0, N-1)
    tension[i] += lambda_val
    threshold = threshold_base * deg[i] / (1 + a_SOC * lambda_val)
    if tension[i] > threshold:
        size, unique = run_avalanche(i)
        R0_current = size / unique if size > 1 else 0.0
        R0_smooth = (1 - alpha_smooth) * R0_smooth + alpha_smooth * R0_current
        lambda_val += eta_lambda * (1.0 - R0_smooth)
        lambda_val = max(0.01, lambda_val)
    if t == T_equil:
        for e in edge_weight_sum:
            edge_weight_sum[e] = 0.0
    if t >= T_equil and (t - T_equil) % snap_interval == 0:
        snap = {}
        for (u, v) in G.edges():
            snap[(u, v)] = edge_weight_sum[(u, v)]
            snap[(v, u)] = edge_weight_sum[(v, u)]
        snapshots.append(snap)

print(f"λ final ≈ {lambda_val:.3f}, R0 final ≈ {R0_smooth:.3f}")
print(f"Snapshots coletados: {len(snapshots)}")

if len(snapshots) < 5:
    print("Erro: Snapshots insuficientes.")
    exit()

# =========================================
# PESOS MÉDIOS
# =========================================
avg_weight = {}
for e in G.edges():
    avg_weight[e] = edge_weight_sum[e] / max(1, T_sample)
    avg_weight[(e[1], e[0])] = avg_weight[e]
max_w = max(avg_weight.values()) if avg_weight else 1.0
if max_w > 0:
    for e in avg_weight:
        avg_weight[e] /= max_w

# =========================================
# CICLOS E CLUSTER
# =========================================
def find_fundamental_cycles(G):
    T = nx.bfs_tree(G, source=list(G.nodes())[0])
    T_undirected = T.to_undirected()
    cycles = []
    for (u, v) in G.edges():
        if not T_undirected.has_edge(u, v):
            try:
                path = nx.shortest_path(T_undirected, u, v)
                edges = [(path[k], path[k+1]) for k in range(len(path)-1)]
                edges.append((v, u))
                cycles.append(edges)
            except:
                pass
    return cycles

cycles = find_fundamental_cycles(G)
print(f"Total de ciclos: {len(cycles)}")

# Selecionar cluster: hubs com mais ciclos
hubs = sorted(G.degree, key=lambda x: x[1], reverse=True)
cluster = []
for hub, _ in hubs:
    for c in cycles:
        if any(hub in e for e in c):
            cluster.append(c)
    if len(cluster) >= 64:
        break
cluster = cluster[:64]

if len(cluster) == 0:
    print("Aviso: Nenhum hub encontrado nos ciclos. Usando todos os ciclos como cluster.")
    cluster = cycles[:64]

print(f"Cluster size: {len(cluster)}")

def build_edge_maps(cluster):
    maps = []
    for cycle in cluster:
        m = {}
        for (u, v) in cycle:
            key = (min(u, v), max(u, v))
            sign = 1 if (u, v) == key else -1
            m[key] = sign
        maps.append(m)
    return maps

edge_maps = build_edge_maps(cluster)
K = len(edge_maps)

# =========================================
# TENSOR F (ponderado pelos pesos SOC)
# =========================================
F_raw = torch.zeros((K, K, K), device=device)
edge_to_cycles = {}
for i, m in enumerate(edge_maps):
    for e in m:
        edge_to_cycles.setdefault(e, []).append(i)

for e, idxs in edge_to_cycles.items():
    w = avg_weight.get(e, 0.0)
    for i in idxs:
        for j in idxs:
            for k in idxs:
                F_raw[i, j, k] += edge_maps[i][e] * edge_maps[j][e] * edge_maps[k][e] * w

mx = F_raw.abs().max()
if mx > 0:
    F_raw /= mx

# =========================================
# MÉTRICA (COVARIÂNCIA AMOSTRAL)
# =========================================
S_C_samples = np.zeros((len(snapshots), K))
for sn, snap in enumerate(snapshots):
    S = np.zeros(K)
    for idx, cycle_map in enumerate(edge_maps):
        for e, sign in cycle_map.items():
            S[idx] += sign * snap.get(e, 0.0)
    S_C_samples[sn] = S

g_np = np.cov(S_C_samples.T)
g = torch.tensor(g_np, dtype=torch.float32, device=device)
mxg = g.abs().max()
if mxg > 0:
    g /= mxg

# =========================================
# TENSOR DE LIE (colchete de Poisson efetivo)
# =========================================
F_lie = torch.einsum('jm, imk -> ijk', g, F_raw) - torch.einsum('im, jmk -> ijk', g, F_raw)

# =========================================
# OTIMIZAÇÃO E CLASSIFICAÇÃO (mesmo pipeline V4.9)
# =========================================
def jacobi_error(F):
    t1 = torch.einsum('abd,dce->abce', F, F)
    t2 = torch.einsum('bcd,dae->abce', F, F)
    t3 = torch.einsum('cad,dbe->abce', F, F)
    return (t1 + t2 + t3).pow(2).mean()

def closure_error_diff(F_lie, alpha):
    brackets = torch.einsum('ai,bj,ijk->abk', alpha, alpha, F_lie)
    coef = torch.einsum('ai,abk->ibk', alpha, brackets)
    proj = torch.einsum('ai,ibk->abk', alpha, coef)
    return (brackets - proj).pow(2).mean()

def compute_F_ext(cluster_maps, all_maps, samples=30):
    K = len(cluster_maps)
    external = [m for m in all_maps if m not in cluster_maps]
    if len(external) == 0:
        return torch.zeros((K, K, 1), device=device)
    sampled = random.sample(external, min(samples, len(external)))
    M = len(sampled)
    F_ext = torch.zeros((K, K, M), device=device)
    for m_idx, ext in enumerate(sampled):
        for e, ext_val in ext.items():
            for i in range(K):
                if e in cluster_maps[i]:
                    for j in range(K):
                        if e in cluster_maps[j]:
                            F_ext[i, j, m_idx] += cluster_maps[i][e] * cluster_maps[j][e] * ext_val * avg_weight.get(e, 0.0)
    mx = F_ext.abs().max()
    if mx > 0:
        F_ext /= mx
    return F_ext

all_maps = build_edge_maps(cycles)
F_ext = compute_F_ext(edge_maps, all_maps)

def optimize_blind(F_lie, g, F_ext, epochs=2000):
    K = F_lie.shape[0]
    alpha = torch.nn.Parameter(0.01 * torch.randn((8, K), device=device))
    optimizer = optim.Adam([alpha], lr=1e-3)
    for epoch in range(epochs):
        optimizer.zero_grad()
        alpha_norm = alpha / (alpha.norm(dim=1, keepdim=True) + 1e-8)
        Ftilde = torch.einsum('ai,bj,ck,ijk->abc', alpha_norm, alpha_norm, alpha_norm, F_lie)
        Ftilde = Ftilde / (Ftilde.norm() + 1e-8)
        loss_clos = closure_error_diff(F_lie, alpha_norm)
        loss_jac = jacobi_error(Ftilde)
        Gproj = torch.einsum('ai,ij,bj->ab', alpha_norm, g, alpha_norm)
        loss_ort = (Gproj - torch.eye(8, device=device)).pow(2).mean()
        loss_ext = torch.einsum('ai,bj,ijm->abm', alpha_norm, alpha_norm, F_ext).pow(2).mean()
        K_ftilde = torch.einsum('acd,bcd->ab', Ftilde, Ftilde)
        eigvals_K = torch.abs(torch.linalg.eigvalsh(K_ftilde))
        loss_dim = -torch.sum(torch.log(eigvals_K + 1e-8))
        loss_rank = (eigvals_K < 1e-4).float().mean()
        loss = (loss_clos + 0.1 * loss_jac + 0.1 * loss_ort + 0.01 * loss_ext
                + 0.01 * loss_dim + 0.1 * loss_rank)
        loss.backward()
        torch.nn.utils.clip_grad_norm_([alpha], 1.0)
        optimizer.step()
        if epoch % 200 == 0:
            print(f"Epoch {epoch:4d} | Loss: {loss.item():.6f} | Clos: {loss_clos.item():.6f}")
    return Ftilde.detach(), alpha_norm.detach(), Gproj.detach(), loss.item()

def classify_algebra(Ftilde, Gproj):
    Kf = torch.einsum('acd,bcd->ab', Ftilde, Ftilde)
    vals, vecs = torch.linalg.eigh(Kf)
    idx = torch.argsort(vals.abs(), descending=True)
    vals, vecs = vals[idx], vecs[:, idx]
    ratios = vals.abs()[:-1] / (vals.abs()[1:] + 1e-8)
    if (ratios > 5.0).any():
        d = (ratios > 5.0).int().argmax().item() + 1
    else:
        d = (vals.abs() > 1e-3 * vals.abs().max()).sum().item()
    d = max(d, 1)
    V = vecs[:, :d]
    F_active = torch.einsum('ia,jb,kc,abc->ijk', V.T, V.T, V.T, Ftilde)

    def chev(name):
        N = torch.zeros((8,8,8), device=device)
        def add(a,b,c,v):
            N[a,b,c] = v; N[b,a,c] = -v
        if name == 'A1':
            add(0,1,2,1); add(0,2,1,-1); add(1,2,0,1)
        elif name == 'A2':
            add(0,2,2,2); add(0,3,3,-2); add(1,2,2,-1); add(1,3,3,1)
            add(0,4,4,-1); add(0,5,5,1); add(1,4,4,2); add(1,5,5,-2)
            add(0,6,6,1); add(0,7,7,-1); add(1,6,6,1); add(1,7,7,-1)
            add(2,4,6,1); add(2,7,5,-1); add(4,7,3,1)
            add(2,3,0,1); add(4,5,1,1); add(6,7,0,1); add(6,7,1,1)
        return N

    best = None
    best_score = float('inf')
    results = {}
    for name in ['A1', 'A2']:
        N_ref = chev(name)
        d_ref = N_ref.shape[0]
        if d != d_ref:
            results[name] = {'score': float('inf'), 'note': f'dim mismatch (d={d} vs d_ref={d_ref})'}
            continue
        P = torch.nn.Parameter(torch.eye(d, device=device))
        opt_P = optim.Adam([P], lr=0.01)
        for _ in range(300):
            opt_P.zero_grad()
            Q, _ = torch.linalg.qr(P + 1e-6*torch.eye(d, device=device))
            Pinv = Q.T
            N_trans = torch.einsum('ai,bj,kc,ijk->abc', Q, Q, Pinv, N_ref[:d,:d,:d])
            loss_align = (F_active - N_trans).pow(2).mean()
            loss_align.backward()
            opt_P.step()
        with torch.no_grad():
            Q, _ = torch.linalg.qr(P + 1e-6*torch.eye(d, device=device))
            N_trans = torch.einsum('ai,bj,kc,ijk->abc', Q, Q, Q.T, N_ref[:d,:d,:d])
            dist = (F_active - N_trans).pow(2).mean().item()
        results[name] = {'score': dist}
        if dist < best_score:
            best_score = dist
            best = name
    return best, best_score, results, d

Ftilde, alpha, Gproj, loss = optimize_blind(F_lie, g, F_ext)
name, score, results, d_active = classify_algebra(Ftilde, Gproj)
print(f"\n=== RESULTADO FASE II ===")
print(f"Dimensão ativa: {d_active}")
print(f"Álgebra identificada: {name} | Score: {score:.6f} | Loss final: {loss:.6f}")
for n, r in results.items():
    if 'note' in r:
        print(f"  {n}: {r['note']}")
    else:
        print(f"  {n}: score={r['score']:.6f}")
print("Lambda Phi-SOC V2.0 Fase II concluída.")