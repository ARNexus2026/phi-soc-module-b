def find_fundamental_cycles(G):
    T = nx.bfs_tree(G, source=list(G.nodes())[0])
    T_undirected = T.to_undirected()
    cycles = []
    for (u, v) in G.edges():
        if not T_undirected.has_edge(u, v):
            try:
                path = nx.shortest_path(T_undirected, u, v)
                edges = [(path[k], path[k+1]) for k in range(len(path)-1)]
                edges.append((v, u))
                cycles.append(edges)
            except:
                pass
    return cycles

def generate_graphs(sizes, seed=42):
    random.seed(seed)
    np.random.seed(seed)
    graphs_raw = []
    for g, size in enumerate(sizes):
        G = nx.barabasi_albert_graph(size, m, seed=seed+g*1000)
        mapping = {old: f"G{g}_{old}" for old in G.nodes()}
        G = nx.relabel_nodes(G, mapping)
        graphs_raw.append(G)
    return graphs_raw

def add_cross_edges(G_global, graphs_raw, num_edges, edge_signs=None, seed=42):
    rng = np.random.RandomState(seed)
    nodes0 = list(graphs_raw[0].nodes())
    nodes1 = list(graphs_raw[1].nodes())
    added = 0
    if edge_signs is None:
        edge_signs = {}
    while added < num_edges:
        u = rng.choice(nodes0)
        v = rng.choice(nodes1)
        if not G_global.has_edge(u, v):
            G_global.add_edge(u, v)
            s = rng.choice([1, -1])
            edge_signs[(u, v)] = s
            edge_signs[(v, u)] = s
            added += 1
    return G_global, edge_signs

def build_clusters(cycles_list, max_per_graph=30):
    clusters = [sorted(cycles, key=len, reverse=True)[:max_per_graph] for cycles in cycles_list]
    all_cycles, cycle_to_graph = [], []
    for g, c in enumerate(clusters):
        for cycle in c:
            all_cycles.append(cycle)
            cycle_to_graph.append(g)
    return clusters, all_cycles, cycle_to_graph

def build_edge_maps(cycles, edge_signs=None):
    maps = []
    for cycle in cycles:
        m = {}
        for (u, v) in cycle:
            key = (min(u, v), max(u, v))
            base_sign = 1 if (u, v) == key else -1
            if edge_signs is not None:
                edge_s = edge_signs.get((u, v), edge_signs.get((v, u), 1))
            else:
                edge_s = 1
            m[key] = base_sign * edge_s
        maps.append(m)
    return maps

def build_S_series(clusters_fixed, all_cycles_fixed, cycle_to_graph_fixed,
                   edge_maps_fixed, snapshots):
    K = len(all_cycles_fixed)
    n_snaps = len(snapshots)
    S_series = np.zeros((n_snaps, K))
    offset = 0
    for cluster in clusters_fixed:
        k = len(cluster)
        for sn, snap in enumerate(snapshots):
            for idx, cycle_map in enumerate(edge_maps_fixed[offset:offset+k]):
                for e, sign in cycle_map.items():
                    S_series[sn, offset+idx] += sign * snap.get(e, 0.0)
        offset += k
    return S_series

def run_soc_simulation_frustrated(G_global, graphs_raw, EPSILON_CROSS,
                                  T_equil, T_sample, snap_interval,
                                  add_edge_prob, tear_prob, inversion_prob,
                                  competition_strength, max_avalanche_size,
                                  dissipative=True, edge_signs=None, seed=42):
    random.seed(seed)
    np.random.seed(seed)
    rng = np.random.RandomState(seed)

    all_nodes = list(G_global.nodes())
    node_to_idx = {node: i for i, node in enumerate(all_nodes)}
    idx_to_node = {i: node for node, i in node_to_idx.items()}
    node_to_graph = {}
    for g, G in enumerate(graphs_raw):
        for node in G.nodes():
            node_to_graph[node] = g

    total_nodes = len(all_nodes)
    deg = dict(G_global.degree())
    deg_internal = {}
    for node in all_nodes:
        g = node_to_graph[node]
        deg_internal[node] = max(1, sum(1 for n in G_global.neighbors(node) if node_to_graph.get(n) == g))

    nodes_per_graph = [sorted(list(G.nodes())) for G in graphs_raw]
    tension = [np.zeros(len(nodes_per_graph[g])) for g in range(num_graphs)]
    node_signs = [np.ones(len(nodes_per_graph[g])) for g in range(num_graphs)]
    for g in range(num_graphs):
        for i in range(len(nodes_per_graph[g])):
            if rng.random() < 0.2:
                node_signs[g][i] = -1.0

    lambda_block = np.ones(num_graphs)
    R0_block = np.ones(num_graphs)
    local_idx = [{node: i for i, node in enumerate(nodes_per_graph[g])} for g in range(num_graphs)]

    edge_weight_sum = {e: 0.0 for e in G_global.edges()}
    edge_weight_sum.update({(v, u): 0.0 for (u, v) in G_global.edges()})
    snapshots = []
    host_internal_edges = [(u, v) for (u, v) in G_global.edges()
                           if node_to_graph[u] == 1 and node_to_graph[v] == 1]

    def run_avalanche(start_node):
        g_start = node_to_graph[start_node]
        start_local = local_idx[g_start][start_node]
        queue = deque([(start_node, g_start, start_local)])
        visited = set([start_node])
        total_topplings = 0
        while queue and total_topplings < max_avalanche_size:
            node_i, g_i, i_local = queue.popleft()
            total_topplings += 1
            for neighbor in G_global.neighbors(node_i):
                g_j = node_to_graph.get(neighbor, -1)
                if g_j == -1:
                    continue
                j_local = local_idx[g_j][neighbor]

                sign_factor = node_signs[g_i][i_local] * node_signs[g_j][j_local]
                transfer = sign_factor * (0.3 + b_SOC * lambda_block[g_i]) * tension[g_i][i_local] / deg_internal[node_i]

                if g_i == 0 and g_j == 1:
                    transfer *= EPSILON_CROSS
                    if tear_prob > 0 and len(host_internal_edges) > m * N_per_graph // 2:
                        if rng.random() < tear_prob:
                            incident = [e for e in host_internal_edges if neighbor in e]
                            if incident:
                                e_remove = incident[rng.randint(len(incident))]
                                if G_global.has_edge(*e_remove):
                                    G_global.remove_edge(*e_remove)
                                    host_internal_edges.remove(e_remove)
                                    for v in e_remove:
                                        deg_internal[v] = max(1, sum(1 for n in G_global.neighbors(v) if node_to_graph.get(n) == 1))
                                    if rng.random() < inversion_prob:
                                        node_signs[g_j][j_local] *= -1.0
                elif g_i == 1 and g_j == 0:
                    transfer *= 0.01

                tension[g_j][j_local] += transfer
                tension[g_j][j_local] = min(tension[g_j][j_local], MAX_TENSION)
                edge_weight_sum[(node_i, neighbor)] += transfer
                edge_weight_sum[(neighbor, node_i)] += transfer

                if dissipative:
                    if g_j == 0:
                        thresh = threshold_base * deg[neighbor] / (1 + a_SOC * lambda_block[g_j])
                    else:
                        thresh = threshold_base * (deg[neighbor] ** 1.5) / (1 + a_SOC * lambda_block[g_j])
                    if tension[g_j][j_local] > thresh and neighbor not in visited:
                        visited.add(neighbor)
                        queue.append((neighbor, g_j, j_local))
                else:
                    if neighbor not in visited:
                        visited.add(neighbor)
                        queue.append((neighbor, g_j, j_local))

            if dissipative:
                tension[g_i][i_local] *= 0.1 / (1 + c_SOC * lambda_block[g_i])
            tension[g_i][i_local] = min(tension[g_i][i_local], MAX_TENSION)
        return total_topplings, len(visited)

    for t in range(T_equil + T_sample):
        if competition_strength > 0:
            for i in range(min(len(nodes_per_graph[0]), len(nodes_per_graph[1]))):
                tension[0][i] -= competition_strength * tension[1][i]
                tension[1][i] -= competition_strength * tension[0][i]
                tension[0][i] = max(0.0, tension[0][i])
                tension[1][i] = max(0.0, tension[1][i])

        if add_edge_prob > 0 and rng.random() < add_edge_prob:
            u = rng.choice(nodes_per_graph[0])
            v = rng.choice(nodes_per_graph[1])
            if not G_global.has_edge(u, v):
                G_global.add_edge(u, v)
                s = rng.choice([1, -1])
                if edge_signs is not None:
                    edge_signs[(u, v)] = s
                    edge_signs[(v, u)] = s
                edge_weight_sum[(u, v)] = 0.0
                edge_weight_sum[(v, u)] = 0.0
                deg[u] = G_global.degree(u)
                deg[v] = G_global.degree(v)
                deg_internal[u] = max(1, sum(1 for n in G_global.neighbors(u) if node_to_graph.get(n) == 0))
                deg_internal[v] = max(1, sum(1 for n in G_global.neighbors(v) if node_to_graph.get(n) == 1))

        g = rng.randint(num_graphs)
        u = rng.choice(nodes_per_graph[g])
        i_local = local_idx[g][u]
        tension[g][i_local] += lambda_block[g] * node_signs[g][i_local]
        tension[g][i_local] = min(abs(tension[g][i_local]), MAX_TENSION) * np.sign(tension[g][i_local])

        if dissipative:
            threshold = threshold_base * deg[u] / (1 + a_SOC * lambda_block[g])
            if abs(tension[g][i_local]) > threshold:
                size, unique = run_avalanche(u)
                R0_current = size / unique if size > 1 else 0.0
                R0_block[g] = (1 - alpha_smooth) * R0_block[g] + alpha_smooth * R0_current
                lambda_block[g] += eta_lambda * (1.0 - R0_block[g])
                lambda_block[g] = max(0.01, lambda_block[g])
                mean_R0 = np.mean(R0_block)
                for k in range(num_graphs):
                    lambda_block[k] += GAMMA_COMPETITION * (R0_block[k] - mean_R0)
                    lambda_block[k] = max(0.01, lambda_block[k])
        else:
            if abs(tension[g][i_local]) > threshold_base * deg[u] / (1 + a_SOC * lambda_block[g]):
                size, unique = run_avalanche(u)

        if t == T_equil:
            for e in edge_weight_sum:
                edge_weight_sum[e] = 0.0
        if t >= T_equil and (t - T_equil) % snap_interval == 0:
            snap = {e: edge_weight_sum[e] for e in G_global.edges()}
            snap.update({(v, u): edge_weight_sum[(v, u)] for (u, v) in G_global.edges()})
            snapshots.append(snap)

    return snapshots, lambda_block, R0_block

def poisson_bracket_omega(S_series):
    n_snaps, K = S_series.shape
    omega = np.zeros((K, K))
    for t in range(n_snaps - 1):
        x = S_series[t]
        dx = S_series[t+1] - S_series[t]
        omega += np.outer(x, dx) - np.outer(dx, x)
    omega /= (n_snaps - 1)
    return (omega - omega.T) / 2

def estimate_derivatives_C(S_series):
    n_snaps, K = S_series.shape
    window = 10
    X_w, Y_w = [], []
    for t in range(0, n_snaps - window, window):
        sub = S_series[t:t+window]
        w = poisson_bracket_omega(sub)
        X_w.append(sub.mean(axis=0))
        Y_w.append(w.flatten())
    if len(X_w) < K:
        X_w = [S_series.mean(axis=0)]
        Y_w = [poisson_bracket_omega(S_series).flatten()]
    X_arr, Y_arr = np.array(X_w), np.array(Y_w)
    XtX = X_arr.T @ X_arr + 1e-8 * np.eye(K)
    XtY = X_arr.T @ Y_arr
    C_flat = np.linalg.solve(XtX, XtY).T
    return C_flat.reshape(K, K, K)

def poisson_jacobi_direct(omega, C):
    K = omega.shape[0]
    J = np.zeros((K, K, K))
    for i in range(K):
        for j in range(K):
            for k in range(K):
                s = 0.0
                for l in range(K):
                    s += omega[i,l] * C[j,k,l] + omega[j,l] * C[k,i,l] + omega[k,l] * C[i,j,l]
                J[i,j,k] = s
    norm_J = np.sum(J**2)
    norm_omega = np.sum(omega**2)
    norm_C = np.sum(C**2)
    return norm_J / (norm_omega * norm_C + 1e-8)

