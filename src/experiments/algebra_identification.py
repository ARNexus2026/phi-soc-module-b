def gell_mann_f():
    """Retorna as constantes de estrutura f^{abc} de SU(3) na base de Gell‑Mann."""
    f = np.zeros((8, 8, 8))
    entries = [
        (0,1,2, 1.0),
        (0,3,6, 0.5), (0,4,5, -0.5),
        (1,3,5, 0.5), (1,4,6, 0.5),
        (2,3,4, 0.5), (2,5,6, -0.5),
        (3,4,7, np.sqrt(3)/2), (5,6,7, np.sqrt(3)/2)
    ]
    for a,b,c,val in entries:
        f[a,b,c] = val; f[b,c,a] = val; f[c,a,b] = val
        f[a,c,b] = -val; f[c,b,a] = -val; f[b,a,c] = -val
    return f

