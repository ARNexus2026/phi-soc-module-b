def generate_graph(N, m=2, seed=None):
    if seed is not None:
        G = nx.barabasi_albert_graph(N, m, seed=seed)
    else:
        G = nx.barabasi_albert_graph(N, m)
    return G

def generate_graphs(sizes, m=2, seed=42):
    set_seed(seed)
    graphs_raw = []
    for g, size in enumerate(sizes):
        G = nx.barabasi_albert_graph(size, m, seed=seed + g*1000)
        mapping = {old: f"G{g}_{old}" for old in G.nodes()}
        G = nx.relabel_nodes(G, mapping)
        graphs_raw.append(G)
    return graphs_raw

def add_cross_edges(G_global, graphs_raw, num_edges, edge_signs=None, seed=42):
    rng = np.random.RandomState(seed)
    nodes0 = list(graphs_raw[0].nodes())
    nodes1 = list(graphs_raw[1].nodes())
    if edge_signs is None:
        edge_signs = {}
    added = 0
    while added < num_edges:
        u = rng.choice(nodes0)
        v = rng.choice(nodes1)
        if not G_global.has_edge(u, v):
            G_global.add_edge(u, v)
            s = rng.choice([1, -1])
            edge_signs[(u, v)] = s
            edge_signs[(v, u)] = s
            added += 1
    return G_global, edge_signs

def couple_graphs(G_list, cross_edges_per_pair=5, seed=42):
    G_global = nx.Graph()
    for G in G_list:
        G_global = nx.compose(G_global, G)
    num_graphs = len(G_list)
    nodes_per_graph = [sorted(list(G.nodes())) for G in G_list]
    node_graph = {}
    for g, nodes in enumerate(nodes_per_graph):
        for node in nodes:
            node_graph[node] = g
    rng = np.random.RandomState(seed)
    for g1 in range(num_graphs):
        for g2 in range(g1+1, num_graphs):
            for _ in range(cross_edges_per_pair):
                u = rng.choice(nodes_per_graph[g1])
                v = rng.choice(nodes_per_graph[g2])
                if not G_global.has_edge(u, v):
                    G_global.add_edge(u, v)
    return G_global, node_graph, nodes_per_graph

def generate_and_couple_graphs(sizes, m=2, cross_edges_per_pair=5, seed=42):
    graphs_raw = generate_graphs(sizes, m, seed)
    G_global, node_graph, nodes_per_graph = couple_graphs(graphs_raw, cross_edges_per_pair, seed)
    return G_global, node_graph, nodes_per_graph, graphs_raw

