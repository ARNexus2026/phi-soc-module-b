def sweep_parameters(param_grid, sizes, T_equil, T_sample, snap_interval,
                     max_cycles=50, seed=42):
    """
    Varre uma grelha de parâmetros e devolve as métricas de Jacobi e
    rank efectivo para cada ponto.  A dimensão do subespaço PCA é determinada
    automaticamente pela dimensão ativa da forma de Killing de C_red.
    """
    results = []
    for params in param_grid:
        print(f"Testando parâmetros: {params}")
        graphs_raw = generate_graphs(sizes, m=2, seed=seed)
        G_global, node_graph, nodes_per_graph = couple_graphs(graphs_raw, 5, seed)
        snapshots, _, _ = run_soc_simulation(
            G_global, node_graph, nodes_per_graph,
            EPSILON_CROSS=params.get('EPSILON_CROSS', 0.01),
            T_equil=T_equil, T_sample=T_sample, snap_interval=snap_interval,
            dissipative=params.get('dissipative', True),
            frustration=params.get('frustration', False),
            tear_prob=params.get('tear_prob', 0.0),
            inversion_prob=params.get('inversion_prob', 0.3),
            add_edge_prob=params.get('add_edge_prob', 0.0),
            competition_strength=params.get('competition_strength', 0.0),
            max_avalanche_size=params.get('max_avalanche_size', None),
            seed=seed
        )
        cycles_list = [find_fundamental_cycles(G) for G in graphs_raw]
        clusters, all_cycles, cycle_to_graph = build_clusters(cycles_list, max_per_graph)
        edge_maps = build_edge_maps(all_cycles)
        S_series, F_raw, g, F_lie, offsets = build_SOC_tensors(
            snapshots, clusters, all_cycles, cycle_to_graph, edge_maps,
            EPSILON_BLOCK=params.get('EPSILON_BLOCK', 0.01)
        )
        # Normalização Z‑score e PCA dinâmico
        S_mean = S_series.mean(axis=0)
        S_std = S_series.std(axis=0) + 1e-12
        S_norm = (S_series - S_mean) / S_std
        Cov_S = np.cov(S_norm.T)
        eigvals_cov, eigvecs_cov = np.linalg.eigh(Cov_S)
        idx_cov = np.argsort(eigvals_cov)[::-1]

        # Estimar C no espaço completo (K pode ser grande) e determinar dimensão activa
        omega_full = poisson_bracket_omega(S_norm)
        C_full = estimate_derivatives_C(S_norm)
        F_full = torch.tensor(C_full, dtype=DTYPE, device=device)
        d_active, _, _ = active_dimension(F_full, ratio_threshold=5.0, abs_threshold=1e-3)
        d_proj = min(d_active, len(idx_cov))   # nunca exceder o número de componentes

        # Projecção para a dimensão dinâmica
        V_pca = eigvecs_cov[:, idx_cov[:d_proj]]
        S_red = (S_norm - S_norm.mean(axis=0)) @ V_pca

        omega_red = poisson_bracket_omega(S_red)
        C_red = estimate_derivatives_C(S_red)
        J_poisson = poisson_jacobi_direct(omega_red, C_red)

        F_tensor_red = torch.tensor(C_red.copy(), dtype=DTYPE, device=device)
        K_red = torch.einsum('acd,bcd->ab', F_tensor_red, F_tensor_red)
        eigvals_K, _ = torch.linalg.eigh(K_red)
        rank_eff = (torch.abs(eigvals_K) > 1e-6).sum().item()

        results.append({**params, 'J_poisson': J_poisson, 'rank_eff': rank_eff,
                        'd_active': d_active, 'd_proj': d_proj})
    return results