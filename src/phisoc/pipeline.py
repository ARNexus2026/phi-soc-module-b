def compute_F_lie(F_raw, g):
    """
    Tensor de Lie linearizado (colchete de Poisson efectivo geométrico).
    F_lie = g * F_raw - permutações.
    """
    return torch.einsum('jm, imk -> ijk', g, F_raw) - torch.einsum('im, jmk -> ijk', g, F_raw)

def build_SOC_tensors(snapshots, clusters, all_cycles, cycle_to_graph,
                      edge_maps, EPSILON_BLOCK=0.01):
    S_series = compute_S_series(clusters, all_cycles, cycle_to_graph, edge_maps, snapshots)
    avg_weight = compute_avg_weights_from_snapshots(snapshots)
    F_raw = compute_F_raw(edge_maps, avg_weight, cycle_to_graph, EPSILON_BLOCK, normalize=True)

    K = len(all_cycles)
    S_blocks = []
    offset = 0
    for cluster in clusters:
        k = len(cluster)
        S_blocks.append(S_series[:, offset:offset+k])
        offset += k
    g_np = np.zeros((K, K))
    offsets = [0]
    for S_blk in S_blocks:
        k = S_blk.shape[1]
        blk_cov = np.cov(S_blk.T)
        g_np[offsets[-1]:offsets[-1]+k, offsets[-1]:offsets[-1]+k] = blk_cov
        offsets.append(offsets[-1] + k)
    g = torch.tensor(g_np, dtype=DTYPE, device=device)
    g = g + 1e-4 * torch.eye(K, device=device, dtype=DTYPE)
    g = g / torch.trace(g)
    F_lie = compute_F_lie(F_raw, g)
    return S_series, F_raw, g, F_lie, offsets

