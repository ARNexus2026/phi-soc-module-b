# 7. Parêntesis de Poisson empírico
# ============================================================================
def poisson_bracket_omega(S_series):
    n_snaps, K = S_series.shape
    omega = np.zeros((K, K))
    for t in range(n_snaps - 1):
        x = S_series[t]
        dx = S_series[t+1] - S_series[t]
        omega += np.outer(x, dx) - np.outer(dx, x)
    omega /= (n_snaps - 1)
    return (omega - omega.T) / 2

def estimate_derivatives_C(S_series, lambda_reg=1e-4, window=10):
    """
    Estima ∂_k ω^{ij} por regressão linear com regularização Ridge.
    Nota: a aproximação linear despreza cumulantes de ordem superior;
    a violação residual de Jacobi pode reflectir essas não‑linearidades.
    """
    n_snaps, K = S_series.shape
    X_w, Y_w = [], []
    for t in range(0, n_snaps - window, window):
        sub = S_series[t:t+window]
        w = poisson_bracket_omega(sub)
        X_w.append(sub.mean(axis=0))
        Y_w.append(w.flatten())
    if len(X_w) < K:
        X_w = [S_series.mean(axis=0)]
        Y_w = [poisson_bracket_omega(S_series).flatten()]
    X_arr, Y_arr = np.array(X_w), np.array(Y_w)
    XtX = X_arr.T @ X_arr + lambda_reg * np.eye(K)
    XtY = X_arr.T @ Y_arr
    C_flat = np.linalg.solve(XtX, XtY).T
    return C_flat.reshape(K, K, K)

def poisson_jacobi_direct(omega, C):
    K = omega.shape[0]
    J = np.zeros((K, K, K))
    for i in range(K):
        for j in range(K):
            for k in range(K):
                s = 0.0
                for l in range(K):
                    s += (omega[i, l] * C[j, k, l] +
                          omega[j, l] * C[k, i, l] +
                          omega[k, l] * C[i, j, l])
                J[i, j, k] = s
    norm_J = np.sum(J**2)
    norm_omega = np.sum(omega**2)
    norm_C = np.sum(C**2)
    return norm_J / (norm_omega * norm_C + 1e-8)

def shuffle_baseline(S_series, seed=None):
    rng = np.random.RandomState(seed)
    idx = rng.permutation(S_series.shape[0])
    S_shuffled = S_series[idx]
    omega_sh = poisson_bracket_omega(S_shuffled)
    C_sh = estimate_derivatives_C(S_shuffled)
    return poisson_jacobi_direct(omega_sh, C_sh)

