def chevalley_tensor(algebra='A2'):
    N = torch.zeros((8, 8, 8), device=device, dtype=DTYPE)
    def add(a, b, c, v):
        N[a, b, c] = v
        N[b, a, c] = -v
    if algebra == 'A1':
        add(0,1,2,1); add(0,2,1,-1); add(1,2,0,1)
    elif algebra == 'A2':
        add(0,2,2,2);  add(0,3,3,-2)
        add(0,4,4,-1); add(0,5,5,1)
        add(0,6,6,1);  add(0,7,7,-1)
        add(1,2,2,-1); add(1,3,3,1)
        add(1,4,4,2);  add(1,5,5,-2)
        add(1,6,6,1);  add(1,7,7,-1)
        add(2,4,6,1);  add(2,7,5,-1)
        add(4,7,3,1);  add(2,3,0,1)
        add(4,5,1,1);  add(6,7,0,1)
        add(6,7,1,1)
    return N

def gell_mann_f():
    f = np.zeros((8, 8, 8))
    entries = [
        (0,1,2, 1.0), (0,3,6, 0.5), (0,4,5, -0.5),
        (1,3,5, 0.5), (1,4,6, 0.5), (2,3,4, 0.5),
        (2,5,6, -0.5), (3,4,7, np.sqrt(3)/2), (5,6,7, np.sqrt(3)/2)
    ]
    for a,b,c,val in entries:
        f[a,b,c] = val; f[b,c,a] = val; f[c,a,b] = val
        f[a,c,b] = -val; f[c,b,a] = -val; f[b,a,c] = -val
    return f

def all_reference_tensors():
    return {'A1': chevalley_tensor('A1'), 'A2': chevalley_tensor('A2')}

