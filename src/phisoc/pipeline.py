def canonical_edge(u, v):
    return (min(u, v), max(u, v))

def build_edge_maps(cycles, edge_signs=None):
    maps = []
    for cycle in cycles:
        m = {}
        for (u, v) in cycle:
            key = canonical_edge(u, v)
            base_sign = 1 if (u, v) == key else -1
            s = (edge_signs.get((u, v), edge_signs.get((v, u), 1)) if edge_signs else 1)
            m[key] = base_sign * s
        maps.append(m)
    return maps

def compute_F_raw(edge_maps, avg_weight=None, cycle_to_graph=None,
                  EPSILON_BLOCK=0.01, normalize=True):
    """
    Tensor de tripla interseção F_raw[i,j,k].
    EPSILON_BLOCK controla a mistura entre ciclos de grafos diferentes.
    Se normalize=True, o tensor é normalizado pelo máximo absoluto (geométrico).
    """
    K = len(edge_maps)
    F = torch.zeros((K, K, K), device=device, dtype=DTYPE)
    edge_to_cycles = {}
    for i, m in enumerate(edge_maps):
        for e in m:
            edge_to_cycles.setdefault(e, []).append(i)
    for e, idxs in edge_to_cycles.items():
        w = avg_weight.get(e, 1.0) if avg_weight else 1.0
        for i in idxs:
            for j in idxs:
                for k in idxs:
                    val = edge_maps[i][e] * edge_maps[j][e] * edge_maps[k][e] * w
                    if cycle_to_graph is not None:
                        gi, gj, gk = cycle_to_graph[i], cycle_to_graph[j], cycle_to_graph[k]
                        if gi != gj or gj != gk:
                            val *= EPSILON_BLOCK
                    F[i, j, k] += val
    if normalize:
        mx = F.abs().max()
        if mx > 0:
            F /= mx
    return F

def compute_metric_intersection(edge_maps):
    K = len(edge_maps)
    g = torch.zeros((K, K), device=device, dtype=DTYPE)
    for i in range(K):
        for j in range(K):
            g[i, j] = len(set(edge_maps[i]) & set(edge_maps[j]))
    mx = g.abs().max()
    if mx > 0:
        g /= mx
    return g

def compute_metric_covariance(S_series):
    g_np = np.cov(S_series.T)
    g = torch.tensor(g_np, dtype=DTYPE, device=device)
    g = g + 1e-4 * torch.eye(g.shape[0], device=device, dtype=DTYPE)
    g = g / torch.trace(g)
    return g

