def jacobi_error_relative_tensor(F):
    t1 = torch.einsum('abd,dce->abce', F, F)
    t2 = torch.einsum('bcd,dae->abce', F, F)
    t3 = torch.einsum('cad,dbe->abce', F, F)
    J = t1 + t2 + t3
    return J.pow(2).mean().item() / (F.pow(2).mean().item()**2 + 1e-8)

def jacobi_error(F):
    t1 = torch.einsum('abd,dce->abce', F, F)
    t2 = torch.einsum('bcd,dae->abce', F, F)
    t3 = torch.einsum('cad,dbe->abce', F, F)
    return (t1 + t2 + t3).pow(2).mean()

def closure_error_diff(F_lie, alpha):
    brackets = torch.einsum('ai,bj,ijk->abk', alpha, alpha, F_lie)
    coef = torch.einsum('ai,abk->ibk', alpha, brackets)
    proj = torch.einsum('ai,ibk->abk', alpha, coef)
    return (brackets - proj).pow(2).mean()

def compute_killing_like(F):
    return torch.einsum('acd,bcd->ab', F, F)

def active_dimension(Ftilde, ratio_threshold=5.0, abs_threshold=1e-3):
    Kf = torch.einsum('acd,bcd->ab', Ftilde, Ftilde)
    vals, vecs = torch.linalg.eigh(Kf)
    idx = torch.argsort(vals.abs(), descending=True)
    vals, vecs = vals[idx], vecs[:, idx]
    ratios = vals.abs()[:-1] / (vals.abs()[1:] + 1e-8)
    if (ratios > ratio_threshold).any():
        d = (ratios > ratio_threshold).int().argmax().item() + 1
    else:
        d = (vals.abs() > abs_threshold * vals.abs().max()).sum().item()
    return max(d, 1), vals, vecs

def classify_algebra(Ftilde, ratio_threshold=5.0, abs_threshold=1e-3,
                     epochs_align=500, n_starts=5):
    d_active, vals, vecs = active_dimension(Ftilde, ratio_threshold, abs_threshold)
    V = vecs[:, :d_active]
    F_active = torch.einsum('ia,jb,kc,abc->ijk', V.T, V.T, V.T, Ftilde)
    refs = all_reference_tensors()
    best, best_score = None, float('inf')
    results = {}
    for name, N_ref in refs.items():
        d_ref = N_ref.shape[0]
        if d_active < d_ref:
            results[name] = {'score': float('inf'), 'note': f'dim mismatch'}
            continue
        F_comp = F_active if d_active == d_ref else F_active
        N_comp = N_ref if d_active == d_ref else \
                 torch.einsum('ia,jb,kc,abc->ijk', vecs[:d_ref,:d_active].T,
                              vecs[:d_ref,:d_active].T, vecs[:d_ref,:d_active].T, N_ref)
        dim = F_comp.shape[0]
        best_dist = float('inf')
        for _ in range(n_starts):
            P = torch.nn.Parameter(torch.eye(dim, device=device, dtype=DTYPE))
            opt_P = optim.Adam([P], lr=0.01)
            for _ in range(epochs_align):
                opt_P.zero_grad()
                Q, _ = torch.linalg.qr(P + 1e-6 * torch.eye(dim, device=device, dtype=DTYPE))
                N_trans = torch.einsum('ai,bj,kc,ijk->abc', Q, Q, Q.T, N_comp)
                loss = (F_comp - N_trans).pow(2).mean()
                loss.backward()
                opt_P.step()
            with torch.no_grad():
                Q, _ = torch.linalg.qr(P + 1e-6 * torch.eye(dim, device=device, dtype=DTYPE))
                N_trans = torch.einsum('ai,bj,kc,ijk->abc', Q, Q, Q.T, N_comp)
                dist = (F_comp - N_trans).pow(2).mean().item()
                if dist < best_dist:
                    best_dist = dist
        results[name] = {'score': best_dist}
        if best_dist < best_score:
            best_score = best_dist
            best = name
    return best, best_score, results, d_active

def identify_algebra(C_red, jac_shuffle=None):
    """
    Validação final. Se jac_shuffle for fornecido, compara a violação de Jacobi
    com a linha de base e modula a classificação quasi‑Lie em função da razão.
    """
    dim = C_red.shape[0]
    print(f"\n=== IDENTIFICAÇÃO DE ÁLGEBRA (d={dim}) ===")
    antisym = np.max(np.abs(C_red + C_red.transpose(1,0,2)))
    print(f"Erro de antissimetria: {antisym:.2e}")

    J = np.zeros((dim, dim, dim, dim))
    for a in range(dim):
        for b in range(dim):
            for c in range(dim):
                for d_idx in range(dim):
                    s = 0.0
                    for e in range(dim):
                        s += (C_red[a,b,e] * C_red[e,c,d_idx] +
                              C_red[b,c,e] * C_red[e,a,d_idx] +
                              C_red[c,a,e] * C_red[e,b,d_idx])
                    J[a,b,c,d_idx] = s
    jac_norm = np.sqrt(np.sum(J**2))
    C_norm = np.sqrt(np.sum(C_red**2))
    jac_rel = jac_norm / (C_norm**2 + 1e-12)
    print(f"Jacobi relativo: {jac_rel:.4e}")

    if jac_shuffle is not None:
        ratio = jac_rel / (jac_shuffle + 1e-12)
        print(f"Razão Jacobi / shuffle: {ratio:.2f}")
    else:
        ratio = None

    if dim == 8:
        F_target = gell_mann_f()
        F_input = C_red / (np.linalg.norm(C_red) + 1e-12)
        F_target = F_target / (np.linalg.norm(F_target) + 1e-12)
        T = torch.eye(dim, dtype=DTYPE, device=device, requires_grad=True)
        F_target_t = torch.tensor(F_target, dtype=DTYPE, device=device)
        F_input_t = torch.tensor(F_input, dtype=DTYPE, device=device)
        opt = torch.optim.Adam([T], lr=1e-2)
        best_dist = float('inf')
        for _ in range(500):
            opt.zero_grad()
            Q, _ = torch.linalg.qr(T)
            F_trans = torch.einsum('ai,bj,ck,ijk->abc', Q, Q, Q, F_target_t)
            loss = ((F_trans - F_input_t)**2).mean()
            loss.backward()
            opt.step()
            with torch.no_grad():
                dist = loss.item()
                if dist < best_dist:
                    best_dist = dist
        print(f"Distância Procrustes para SU(3) (Gell‑Mann): {best_dist:.4e}")
        if best_dist < 1e-4:
            print(">>> ESTRUTURA COMPATÍVEL COM SU(3) (base de Gell‑Mann)")
        elif best_dist < 1e-2:
            print(">>> Aproximação parcial de SU(3)")
        else:
            print(">>> NÃO é compatível com SU(3) canónico")
    else:
        best_dist = None
        print(f"Dimensão {dim} ≠ 8. Não é possível comparar com SU(3).")

    if antisym < 1e-12 and jac_rel < 1e-3:
        print(">>> Estrutura satisfaz os axiomas de álgebra de Lie.")
        if dim == 8:
            print(">>> Pela classificação de Cartan‑Killing, a única álgebra simples compacta de dim 8 é su(3).")
    else:
        if antisym >= 1e-12:
            print(">>> ATENÇÃO: Violação de antissimetria.")
        if jac_rel >= 1e-3:
            if ratio is not None and ratio < 1.0:
                print(f">>> Estrutura quasi‑Lie emergente (Jacobi {jac_rel:.2e}, razão vs. shuffle {ratio:.2f}).")
            else:
                print(f">>> ATENÇÃO: Violação de Jacobi ({jac_rel:.2e}). Estrutura quasi‑Lie emergente.")
    return antisym, jac_rel, best_dist if dim==8 else None

