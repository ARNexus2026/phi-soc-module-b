def compute_S_series(clusters, all_cycles, cycle_to_graph, edge_maps, snapshots):
    K = len(all_cycles)
    n_snaps = len(snapshots)
    S = np.zeros((n_snaps, K))
    offset = 0
    for cluster in clusters:
        k = len(cluster)
        for sn, snap in enumerate(snapshots):
            for idx, cycle_map in enumerate(edge_maps[offset:offset+k]):
                for e, sign in cycle_map.items():
                    S[sn, offset+idx] += sign * snap.get(e, 0.0)
        offset += k
    return S

def compute_avg_weights_from_snapshots(snapshots):
    avg_weight = {}
    if not snapshots:
        return avg_weight
    for e in snapshots[0]:
        vals = [snap.get(e, 0.0) for snap in snapshots]
        avg_weight[e] = np.mean(vals)
    max_w = max(avg_weight.values()) if avg_weight else 1.0
    if max_w > 0:
        for e in avg_weight:
            avg_weight[e] /= max_w
    return avg_weight

