def dimension_stability(Ftilde, ratio_range=(3.0, 10.0), abs_range=(1e-4, 1e-2)):
    results = {}
    for ratio_th in np.linspace(*ratio_range, 5):
        for abs_th in np.logspace(np.log10(abs_range[0]), np.log10(abs_range[1]), 5):
            d, _, _ = active_dimension(Ftilde, ratio_th, abs_th)
            results[(ratio_th, abs_th)] = d
